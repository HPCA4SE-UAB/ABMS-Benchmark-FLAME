library(XML)
df= xmlToDataFrame("EnodeX-100-1.xml", c("character", "double", "double", "double", "double", "double", "double"))
require(ggplot2)
df2 <- data.frame(df$x,df$y)
p <- ggplot(df2, aes(df$x,df$y)) + labs(x = "X") + labs(y = "Y") + labs(title = "FLAME agent distribution")
p <- p + stat_bin2d(bins = 20) + scale_fill_gradient(low = "lightblue", high = "red")
pdf("AgentsDistribFLAME.pdf")
plot(p)
dev.off()
